export function getLangFromUrl(url: URL) {
  const [, lang] = url.pathname.split("/");
  // Check if lang is a valid key in the ui object
  if (lang && lang in ui) {
    return lang as keyof typeof ui;
  }
  return defaultLang;
}

// 2. Add a fallback in useTranslations to prevent the "undefined" crash
export function useTranslations(lang: keyof typeof ui) {
  return function t(key: keyof (typeof ui)[typeof defaultLang]) {
    // Fallback to defaultLang if the provided lang object doesn't exist
    const translations = ui[lang] || ui[defaultLang];
    return translations[key] || ui[defaultLang][key];
  };
}
